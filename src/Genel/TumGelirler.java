    private void btnSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSilActionPerformed
        if (tblTumGelirler.getSelectedRowCount() == 1) {
            try {
                // TODO add your handling code here:
                output = new PrintWriter(new FileWriter("SilinenGelirler.txt", true));
                output.println(dtm.getValueAt(tblTumGelirler.getSelectedRow(), 0) + "`" + dtm.getValueAt(tblTumGelirler.getSelectedRow(), 1) + "`" + dtm.getValueAt(tblTumGelirler.getSelectedRow(), 2) + "`" + dtm.getValueAt(tblTumGelirler.getSelectedRow(), 3) + "`" + dtm.getValueAt(tblTumGelirler.getSelectedRow(), 4) + "`" + dtm.getValueAt(tblTumGelirler.getSelectedRow(), 5));
                output.close();
                dtm.removeRow(tblTumGelirler.getSelectedRow());
                output = new PrintWriter(new FileWriter("Gelir.txt"));
                output.write("");
                output = new PrintWriter(new FileWriter("Gelir.txt", true));
                for (int i = 0; i < tblTumGelirler.getRowCount(); i++) {
                    output.println(dtm.getValueAt(i, 0) + "`" + dtm.getValueAt(i, 1) + "`" + dtm.getValueAt(i, 2) + "`" + dtm.getValueAt(i, 3) + "`" + dtm.getValueAt(i, 4) + "`" + dtm.getValueAt(i, 5));
                }
                output.close();
                JOptionPane.showMessageDialog(rootPane, "Silme tamamlandı");

            } catch (IOException ex) {
                Logger.getLogger(TumGelirler.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Silmeden önce bir seçim yapınız.");
        }
    }//GEN-LAST:event_btnSilActionPerformed

